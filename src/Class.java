public class Class {
    private int nomer;
    private String soz;

    public Class(int nomer, String soz) {
        this.nomer = nomer;
        this.soz = soz;
    }


    public int getNomer() {
        return nomer;
    }

    public void setNomer(int nomer) {
        this.nomer = nomer;
    }

    public String getSoz() {
        return soz;
    }

    public void setSoz(String soz) {
        this.soz = soz;
    }
}




